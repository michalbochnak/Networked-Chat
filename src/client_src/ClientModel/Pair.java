package ClientModel;

import java.io.Serializable;

public class Pair implements Serializable {

    // ------------------------------------------------------------------------
    // Members
    // ------------------------------------------------------------------------
    private int key;
    private int n;

    // ------------------------------------------------------------------------
    // Constructors
    // ------------------------------------------------------------------------
    public Pair(int key, int n) {
        this.key = key;
        this.n = n;
    }


    // ------------------------------------------------------------------------
    // Getters
    // ------------------------------------------------------------------------
    public int getKey() {
        return key;
    }

    public int getN() {
        return n;
    }


    // ------------------------------------------------------------------------
    // Setters
    // ------------------------------------------------------------------------
    public void setKey(int key) {
        this.key = key;
    }

    public void setN(int n) {
        this.n = n;
    }
}
